package com.bookmarker.db.model;

public class Tag {
	private int tagID;
	private int tagName;
	/**
	 * @return the tagID
	 */
	public int getTagID() {
		return tagID;
	}
	/**
	 * @param tagID the tagID to set
	 */
	public void setTagID(int tagID) {
		this.tagID = tagID;
	}
	/**
	 * @return the tagName
	 */
	public int getTagName() {
		return tagName;
	}
	/**
	 * @param tagName the tagName to set
	 */
	public void setTagName(int tagName) {
		this.tagName = tagName;
	}
	
	
}
